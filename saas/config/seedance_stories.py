def get_story_by_theme_and_title(theme: str, title: str) -> dict:
    """Récupère une histoire spécifique par thème et titre"""
    if theme in SEEDANCE_STORIES:
        for story in SEEDANCE_STORIES[theme]["stories"]:
            if story["title"] == title:
                return story
    return None

def get_stories_by_theme(theme: str) -> dict:
    """Récupère toutes les histoires d'un thème"""
    return SEEDANCE_STORIES.get(theme, {})

def get_all_themes() -> list:
    """Récupère tous les thèmes disponibles"""
    return list(SEEDANCE_STORIES.keys())
